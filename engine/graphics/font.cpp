bool Font::create_default() {
    const int CHAR_W = 8;
    const int CHAR_H = 8;
    const int COLS = 16;
    const int ROWS = 6; // ceil(95 / 16)
    const int TEX_W = COLS * CHAR_W; // 128
    const int TEX_H = ROWS * CHAR_H; // 48

    // build RGBA texture
    std::vector<u8> pixels(TEX_W * TEX_H * 4, 0);

    for (int ch = 0; ch < 95; ch++) {
        int col = ch % COLS;
        int row = ch / COLS;

        for (int y = 0; y < CHAR_H; y++) {
            u8 row_bits = BUILTIN_FONT_DATA[ch * CHAR_H + y];
            for (int x = 0; x < CHAR_W; x++) {
                bool on = (row_bits >> (7 - x)) & 1;
                int px = col * CHAR_W + x;
                int py = row * CHAR_H + y;
                int idx = (py * TEX_W + px) * 4;
                pixels[idx + 0] = 255;
                pixels[idx + 1] = 255;
                pixels[idx + 2] = 255;
                pixels[idx + 3] = on ? 255 : 0;
            }
        }

        // register glyph
        char c = static_cast<char>(32 + ch);
        Glyph g;
        g.uv_min = { static_cast<float>(col * CHAR_W) / TEX_W,
                     static_cast<float>(row * CHAR_H) / TEX_H };
        g.uv_max = { static_cast<float>((col + 1) * CHAR_W) / TEX_W,
                     static_cast<float>((row + 1) * CHAR_H) / TEX_H };
        g.size = { static_cast<float>(CHAR_W), static_cast<float>(CHAR_H) };
        g.offset = { 0, 0 };
        g.advance = static_cast<float>(CHAR_W);
        m_glyphs[c] = g;
    }

    // upload to GPU
    u32 tex_id;
    glGenTextures(1, &tex_id);
    glBindTexture(GL_TEXTURE_2D, tex_id);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA8, TEX_W, TEX_H, 0, GL_RGBA, GL_UNSIGNED_BYTE, pixels.data());

    // wrap in our Texture object via a small trick: create a Texture that owns this ID
    // we'll store the raw ID and use it directly
    // the m_atlas won't own it, we manage it manually
    // actually let's just use the atlas texture properly
    // we need to set m_atlas without going through load()

    // hack: create a dummy 1x1 then overwrite its GL state
    // better: add a method to Texture. For now, store font tex ID separately.
    m_atlas.create_white(); // creates a 1x1 white
    // replace the texture data
    glBindTexture(GL_TEXTURE_2D, m_atlas.get_id());
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA8, TEX_W, TEX_H, 0, GL_RGBA, GL_UNSIGNED_BYTE, pixels.data());
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);

    // clean up the separately allocated texture
    glDeleteTextures(1, &tex_id);

    m_line_height = static_cast<float>(CHAR_H);

    log::info("default bitmap font created (8x8, 95 glyphs)");
    return true;
}

bool Font::load_bmfont(const std::string& fnt_path) {
    if (!parse_fnt(fnt_path)) return false;

    // load the atlas texture (same directory as .fnt, page 0)
    // extract directory from fnt_path
    size_t slash = fnt_path.find_last_of("/\\");
    std::string dir = (slash != std::string::npos) ? fnt_path.substr(0, slash + 1) : "";

    // the atlas filename is parsed from the .fnt file
    // for now, assume it's the same name with .png
    std::string atlas_path = fnt_path.substr(0, fnt_path.size() - 4) + "_0.png";
    if (!m_atlas.load(atlas_path, { TextureFilter::Nearest, TextureWrap::ClampToEdge, false })) {
        log::error("font: failed to load atlas '%s'", atlas_path.c_str());
        return false;
    }

    return true;
}

bool Font::parse_fnt(const std::string& path) {
    std::ifstream file(path);
    if (!file.is_open()) {
        log::error("font: failed to open '%s'", path.c_str());
        return false;
    }

    float tex_w = 1, tex_h = 1;
    std::string line;
    while (std::getline(file, line)) {
        std::istringstream ss(line);
        std::string tag;
        ss >> tag;

        if (tag == "common") {
            // parse lineHeight, scaleW, scaleH
            std::string token;
            while (ss >> token) {
                if (token.rfind("lineHeight=", 0) == 0)
                    m_line_height = std::stof(token.substr(11));
                else if (token.rfind("scaleW=", 0) == 0)
                    tex_w = std::stof(token.substr(7));
                else if (token.rfind("scaleH=", 0) == 0)
                    tex_h = std::stof(token.substr(7));
            }
        } else if (tag == "char") {
            int id = 0;
            float x = 0, y = 0, w = 0, h = 0, xoff = 0, yoff = 0, xadv = 0;
            std::string token;
            while (ss >> token) {
                if (token.rfind("id=", 0) == 0) id = std::stoi(token.substr(3));
                else if (token.rfind("x=", 0) == 0) x = std::stof(token.substr(2));
                else if (token.rfind("y=", 0) == 0) y = std::stof(token.substr(2));
                else if (token.rfind("width=", 0) == 0) w = std::stof(token.substr(6));
                else if (token.rfind("height=", 0) == 0) h = std::stof(token.substr(7));
                else if (token.rfind("xoffset=", 0) == 0) xoff = std::stof(token.substr(8));
                else if (token.rfind("yoffset=", 0) == 0) yoff = std::stof(token.substr(8));
                else if (token.rfind("xadvance=", 0) == 0) xadv = std::stof(token.substr(9));
            }

            if (id >= 32 && id <= 126) {
                Glyph g;
                g.uv_min = { x / tex_w, y / tex_h };
                g.uv_max = { (x + w) / tex_w, (y + h) / tex_h };
                g.size = { w, h };
                g.offset = { xoff, yoff };
                g.advance = xadv;
                m_glyphs[static_cast<char>(id)] = g;
            }
        }
    }

    log::info("loaded BMFont: %s (%zu glyphs)", path.c_str(), m_glyphs.size());
    return true;
}

const Glyph* Font::get_glyph(char c) const {
    auto it = m_glyphs.find(c);
    return it != m_glyphs.end() ? &it->second : nullptr;
}

float Font::measure_width(const std::string& text, float scale) const {
    float width = 0;
    for (char c : text) {
        if (auto* g = get_glyph(c)) {
            width += g->advance * scale;
        }
    }
    return width;
}

